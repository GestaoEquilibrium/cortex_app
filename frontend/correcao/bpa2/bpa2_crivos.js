// Faz o parse de um código no mesmo formato do exportador original.
// Retorna { rows, cols, alvos:Set<int> } ou null se inválido.
window.BPA2_parseCrivo = function (codigo) {
    try {
        const partes = String(codigo || '').trim().split('|');
        const [r, c] = partes[1].split('x').map(Number);
        const alvos = new Set(
            (partes[2] || '').split(',').filter(x => x !== '').map(Number)
        );
        if (!r || !c) return null;
        return { rows: r, cols: c, alvos };
    } catch {
        return null;
    }
};

// Crivos já parseados, prontos para uso: { AC:{rows,cols,alvos}, ... }
window.BPA2_CRIVOS = Object.fromEntries(
    Object.entries(window.BPA2_CRIVO_CODES)
        .map(([k, v]) => [k, window.BPA2_parseCrivo(v)])
);
